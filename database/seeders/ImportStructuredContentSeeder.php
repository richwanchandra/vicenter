    private function createFormattedContent(
        Module $module,
        string $title,
        array $lines,
        User $admin
    ): void {
        if (empty($lines)) {
            return;
        }

        $formatted = $this->formatter->format($lines);
        $wrappedHtml = $this->formatter->wrapContent($formatted, $title);

        $existing = Content::where('module_id', $module->id)
            ->where('title', $title)
            ->first();

        if ($existing) {
            $existing->update([
                'content' => $wrappedHtml,
                'updated_by' => $admin->id,
            ]);
            $this->command->line("    ✏️  {$title}");
        } else {
            Content::create([
                'module_id' => $module->id,
                'title' => $title,
                'content' => $wrappedHtml,
                'status' => 'publish',
                'created_by' => $admin->id,
            ]);
            $this->command->line("    ✅ {$title}");
        }
    }

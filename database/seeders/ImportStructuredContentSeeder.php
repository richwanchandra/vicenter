    private ContentFormatter $formatter;

    public function run(): void
    {
        $this->formatter = new ContentFormatter();
        
        $admin = User::where('role', 'super_admin')->first();
        if (!$admin) {
            $this->command->error('No super admin found');
            return;
        }

        $this->command->info('📚 Seeding Structured Content...');

        $contents = $this->getContentMapping();
        $total = 0;

        foreach ($contents as $moduleName => $contentItems) {
            $module = Module::where('name', $moduleName)->first();
            if (!$module) {
                $this->command->warn("⚠ Module not found: {$moduleName}");
                continue;
            }

            foreach ($contentItems as $title => $lines) {
                $this->createFormattedContent($module, $title, $lines, $admin);
                $total++;
            }
        }

        $this->command->info("✅ Content seeding completed! (Total: {$total} items)");
    }

